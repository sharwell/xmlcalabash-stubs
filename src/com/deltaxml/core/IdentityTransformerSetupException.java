package com.deltaxml.core;

/**
 * @see <a href="http://docs.deltaxml.com/core/current/docs/api/com/deltaxml/core/IdentityTransformerSetupException.html">
 * Class IdentityTransformerSetupException</a>
 */
public class IdentityTransformerSetupException extends PipelinedComparatorException {

    public IdentityTransformerSetupException(String message) {
        super(message);
        throw new UnsupportedOperationException();
    }

    public IdentityTransformerSetupException(Throwable t) {
        super(t);
        throw new UnsupportedOperationException();
    }

    public IdentityTransformerSetupException(String message, Throwable t) {
        super(message, t);
        throw new UnsupportedOperationException();
    }

}
